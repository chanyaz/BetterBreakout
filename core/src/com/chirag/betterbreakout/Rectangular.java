package com.chirag.betterbreakout;

public interface Rectangular {
    /**
     * Get the x value
     * @return the x value of the center
     */
    float getX();

    /**
     * Get the y value
     * @return the y value of the center
     */
    float getY();

    /**
     * Get the width of the rectangle
     * @return the width
     */
    float getWidth();

    /**
     * Get the height of the rectangle
     * @return the height
     */
    float getHeight();
}
